<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT SIZE=4 STYLE="font-size: 16pt"><B><FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><SPAN STYLE="text-decoration: none">Chapter
5   The Structure and Function of Macromolecules</SPAN></FONT></FONT></FONT></B></FONT></P>
<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Lecture
Outline</B></I></FONT></FONT></P>
<P STYLE="margin-top: 0.22in; margin-bottom: 0.17in; text-decoration: none">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Overview:
The Molecules of Life</B></FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Within cells, small
	organic molecules are joined together to form larger molecules.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These large
	macromolecules may consist of thousands of covalently bonded atoms
	and weigh more than 100,000 daltons.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The four major
	classes of macromolecules are carbohydrates, lipids, proteins, and
	nucleic acids.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; font-style: normal; line-height: 0.17in">
<BR>
</P>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; font-style: normal; line-height: 0.17in">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Concept
5.1 Most macromolecules are polymers, built from monomers</B></FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Three of the four
	classes of macromolecules—carbohydrates, proteins, and nucleic
	acids—form chainlike molecules called polymers.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A polymer is a long
	molecule consisting of many similar or identical building blocks
	linked by covalent bonds.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	repeated units are small molecules called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>monomers.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Some of the
	molecules that serve as monomers have other functions of their own.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The chemical
	mechanisms that cells use to make and break polymers are similar for
	all classes of macromolecules.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Monomers
	are connected by covalent bonds that form through the loss of a
	water molecule. This reaction is called a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>condensation
	reaction</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	or </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>dehydration
	reaction.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>When a bond forms
	between two monomers, each monomer contributes part of the water
	molecule that is lost. One monomer provides a hydroxyl group (—OH),
	while the other provides a hydrogen (—H).</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cells invest energy
	to carry out dehydration reactions.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The process is
	aided by enzymes.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	covalent bonds connecting monomers in a polymer are disassembled by
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>hydrolysis,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	a reaction that is effectively the reverse of dehydration.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In hydrolysis,
	bonds are broken by the addition of water molecules. A hydrogen atom
	attaches to one monomer, and a hydroxyl group attaches to the
	adjacent monomer.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Our food is taken
	in as organic polymers that are too large for our cells to absorb.
	Within the digestive tract, various enzymes direct hydrolysis of
	specific polymers. The resulting monomers are absorbed by the cells
	lining the gut and transported to the bloodstream for distribution
	to body cells.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The body cells then
	use dehydration reaction to assemble the monomers into new polymers
	that carry out functions specific to the particular cell type.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>An immense
variety of polymers can be built from a small set of monomers.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each cell has
	thousands of different kinds of macromolecules.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These molecules
	vary among cells of the same individual. They vary more among
	unrelated individuals of a species, and even more between species.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This diversity
	comes from various combinations of the 40–50 common monomers and
	some others that occur rarely.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These monomers can
	be connected in a great many combinations, just as the 26 letters in
	the alphabet can be used to create a great diversity of words.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.17in; margin-bottom: 0in; line-height: 0.17in; text-decoration: none">
<BR>
</P>
<P STYLE="margin-top: 0.17in; margin-bottom: 0in; line-height: 0.17in; text-decoration: none">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Concept 5.2
Carbohydrates serve as fuel and building material</B></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Carbohydrates</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	include sugars and their polymers.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The simplest
	carbohydrates are monosaccharides, or simple sugars.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Disaccharides, or
	double sugars, consist of two monosaccharides joined by a
	condensation reaction.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Polysaccharides are
	polymers of many monosaccharides.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Sugars, the
smallest carbohydrates, serve as fuel and a source of carbon.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Monosaccharides</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	generally have molecular formulas that are some multiple of the unit
	CH</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>O.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For
	example, glucose has the formula C</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>6</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>H</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>12</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>O</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>6</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Monosaccharides
	have a carbonyl group (&gt;C=O) and multiple hydroxyl groups (—OH).</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Depending on the
	location of the carbonyl group, the sugar is an aldose or a ketose.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most
	names for sugars end in -</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>ose</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Glucose, an aldose,
	and fructose, a ketose, are structural isomers.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Monosaccharides are
	also classified by the number of carbons in the carbon skeleton.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Glucose and other
	six-carbon sugars are hexoses.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Five-carbon
	backbones are pentoses; three-carbon sugars are trioses.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Monosaccharides may
	also exist as enantiomers.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For example,
	glucose and galactose, both six-carbon aldoses, differ in the
	spatial arrangement of their parts around asymmetrical carbons.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Monosaccharides,
	particularly glucose, are a major fuel for cellular work.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>They also function
	as the raw material for the synthesis of other monomers, such as
	amino acids and fatty acids.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>While often drawn
	as a linear skeleton, monosaccharides in aqueous solutions form
	rings.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Two
	monosaccharides can join with a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>glycosidic
	linkage</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	to form a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>disaccharide</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	via dehydration.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Maltose, malt
	sugar, is formed by joining two glucose molecules.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Sucrose, table
	sugar, is formed by joining glucose and fructose. Sucrose is the
	major transport form of sugars in plants.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Lactose, milk
	sugar, is formed by joining glucose and galactose.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Polysaccharides,
the polymers of sugars, have storage and structural roles.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Polysaccharides</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	are polymers of hundreds to thousands of monosaccharides joined by
	glycosidic linkages.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Some
	polysaccharides serve for storage and are hydrolyzed as sugars are
	needed.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Other
	polysaccharides serve as building materials for the cell or the
	whole organism.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Starch</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is a storage polysaccharide composed entirely of glucose monomers.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most of these
	monomers are joined by 1–4 linkages (number 1 carbon to number 4
	carbon) between the glucose molecules.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The simplest form
	of starch, amylose, is unbranched and forms a helix.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Branched forms such
	as amylopectin are more complex.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Plants store
	surplus glucose as starch granules within plastids, including
	chloroplasts, and withdraw it as needed for energy or carbon.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Animals that feed
	on plants, especially parts rich in starch, have digestive enzymes
	that can hydrolyze starch to glucose.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Animals
	store glucose in a polysaccharide called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>glycogen.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Glycogen is highly
	branched like amylopectin.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Humans and other
	vertebrates store a day’s supply of glycogen in the liver and
	muscles.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Cellulose</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is a major component of the tough wall of plant cells.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Plants produce
	almost one hundred billion tons of cellulose per year. It is the
	most abundant organic compound on Earth.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Like starch,
	cellulose is a polymer of glucose. However, the glycosidic linkages
	in these two polymers differ.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The difference is
	based on the fact that there are actually two slightly different
	ring structures for glucose.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These two ring
	forms differ in whether the hydroxyl group attached to the number 1
	carbon is fixed above (beta glucose) or below (alpha glucose) the
	plane of the ring.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Starch is a
	polysaccharide of alpha glucose monomers.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cellulose is a
	polysaccharide of beta glucose monomers, making every other glucose
	monomer upside down with respect to its neighbors.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The differing
	glycosidic links in starch and cellulose give the two molecules
	distinct three-dimensional shapes.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>While polymers
	built with alpha glucose form helical structures, polymers built
	with beta glucose form straight structures.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The straight
	structures built with beta glucose allow H atoms on one strand to
	form hydrogen bonds with OH groups on other strands.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In plant cell
	walls, parallel cellulose molecules held together in this way are
	grouped into units called microfibrils, which form strong building
	materials for plants (and for humans, as lumber).</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The enzymes that
	digest starch by hydrolyzing its alpha linkages cannot hydrolyze the
	beta linkages in cellulose.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cellulose in human
	food passes through the digestive tract and is eliminated in feces
	as “insoluble fiber.”</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>As it travels
	through the digestive tract, cellulose abrades the intestinal walls
	and stimulates the secretion of mucus, aiding in the passage of
	food.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Some microbes can
	digest cellulose to its glucose monomers through the use of
	cellulase enzymes.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Many eukaryotic
	herbivores, from cows to termites, have symbiotic relationships with
	cellulolytic microbes, providing the microbe and the host animal
	access to a rich source of energy.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Some fungi can also
	digest cellulose.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Another
	important structural polysaccharide is </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>chitin,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	used in the exoskeletons of arthropods (including insects, spiders,
	and crustaceans).</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Chitin is similar
	to cellulose, except that it contains a nitrogen-containing
	appendage on each glucose monomer.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Pure chitin is
	leathery but can be hardened by the addition of calcium carbonate.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Chitin also
	provides structural support for the cell walls of many fungi.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.17in; margin-bottom: 0in; line-height: 0.17in; text-decoration: none">
<BR>
</P>
<P STYLE="margin-top: 0.17in; margin-bottom: 0in; line-height: 0.17in; text-decoration: none">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Concept
5.3 Lipids are a diverse group of hydrophobic molecules</B></FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Unlike other
	macromolecules, lipids do not form polymers.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	unifying feature of </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>lipids</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is that they all have little or no affinity for water.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This is because
	they consist mostly of hydrocarbons, which form nonpolar covalent
	bonds.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Lipids are highly
	diverse in form and function.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Fats store
large amounts of energy.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Although fats are
	not strictly polymers, they are large molecules assembled from
	smaller molecules by dehydration reactions.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>fat</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is constructed from two kinds of smaller molecules: glycerol and
	fatty acids.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Glycerol</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is a three-carbon alcohol with a hydroxyl group attached to each
	carbon.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>fatty
	acid</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	consists of a carboxyl group attached to a long carbon skeleton,
	often 16 to 18 carbons long.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The many nonpolar
	C—H bonds in the long hydrocarbon skeleton make fats hydrophobic.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Fats separate from
	water because the water molecules hydrogen bond to one another and
	exclude the fats.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	a fat, three fatty acids are joined to glycerol by an ester linkage,
	creating a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>triacylglycerol,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	or </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>triglyceride.</I></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The three fatty
	acids in a fat can be the same or different.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Fatty acids may
	vary in length (number of carbons) and in the number and locations
	of double bonds.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>If
	the fatty acid has no carbon-carbon double bonds, then the molecule
	is a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>saturated
	fatty acid,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	saturated with hydrogens at every possible position.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>If
	the fatty acid has one or more carbon-carbon double bonds formed by
	the removal of hydrogen atoms from the carbon skeleton, then the
	molecule is an </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>unsaturated
	fatty acid.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A saturated fatty
	acid is a straight chain, but an unsaturated fatty acid has a kink
	wherever there is a double bond.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Fats made from
	saturated fatty acids are saturated fats.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most animal fats
	are saturated.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Saturated fats are
	solid at room temperature.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Fats made from
	unsaturated fatty acids are unsaturated fats.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Plant and fish fats
	are liquid at room temperature and are known as oils.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The kinks caused by
	the double bonds prevent the molecules from packing tightly enough
	to solidify at room temperature.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The phrase
	“hydrogenated vegetable oils” on food labels means that
	unsaturated fats have been synthetically converted to saturated fats
	by the addition of hydrogen.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Peanut butter and
margarine are hydrogenated to prevent lipids from separating out as
oil.</FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A diet rich in
	saturated fats may contribute to cardiovascular disease
	(atherosclerosis) through plaque deposits.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	process of hydrogenating vegetable oils produces saturated fats and
	also unsaturated fats with </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>trans</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	double bonds. These </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>trans</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	fat molecules contribute more than saturated fats to
	atherosclerosis.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The major function
	of fats is energy storage.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A gram of fat
	stores more than twice as much energy as a gram of a polysaccharide
	such as starch.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Because plants are
	immobile, they can function with bulky energy storage in the form of
	starch. Plants use oils when dispersal and compact storage is
	important, as in seeds.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Animals must carry
	their energy stores with them and benefit from having a more compact
	fuel reservoir of fat.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Humans and other
	mammals store fats as long-term energy reserves in adipose cells
	that swell and shrink as fat is deposited or withdrawn from storage.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Adipose tissue also
	functions to cushion vital organs, such as the kidneys.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A layer of fat can
	also function as insulation.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This subcutaneous
	layer is especially thick in whales, seals, and most other marine
	mammals.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Phospholipids
are major components of cell membranes.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Phospholipids</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	have two fatty acids attached to glycerol and a phosphate group at
	the third position.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The phosphate group
	carries a negative charge.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Additional smaller
	groups may be attached to the phosphate group to form a variety of
	phospholipids.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The interaction of
	phospholipids with water is complex.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The fatty acid
	tails are hydrophobic, but the phosphate group and its attachments
	form a hydrophilic head.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>When phospholipids
	are added to water, they self-assemble into assemblages with the
	hydrophobic tails pointing toward the interior.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This type of
	structure is called a micelle.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Phospholipids are
	arranged as a bilayer at the surface of a cell.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Again, the
	hydrophilic heads are on the outside of the bilayer, in contact with
	the aqueous solution, and the hydrophobic tails point toward the
	interior of the bilayer.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The phospholipid
bilayer forms a barrier between the cell and the external
environment.</FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Phospholipids are
	the major component of all cell membranes.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Steroids
include cholesterol and certain hormones.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Steroids</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	are lipids with a carbon skeleton consisting of four fused rings.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Different steroids
	are created by varying functional groups attached to the rings.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Cholesterol,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	an important steroid, is a component in animal cell membranes.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cholesterol is also
	the precursor from which all other steroids are synthesized.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Many of these other
	steroids are hormones, including the vertebrate sex hormones.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>While cholesterol
	is an essential molecule in animals, high levels of cholesterol in
	the blood may contribute to cardiovascular disease.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Both
	saturated fats and </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>trans</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	fats exert their negative impact on health by affecting cholesterol
	levels.</FONT></FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.17in; margin-bottom: 0in; line-height: 0.17in; text-decoration: none">
<BR>
</P>
<P STYLE="margin-top: 0.17in; margin-bottom: 0in; line-height: 0.17in; text-decoration: none">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Concept
5.4 Proteins have many structures, resulting in a wide range of
functions</B></FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Proteins account
	for more than 50% of the dry mass of most cells. They are
	instrumental in almost everything that an organism does.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Protein functions
	include structural support, storage, transport, cellular signaling,
	movement, and defense against foreign substances.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most
	important, protein enzymes function as </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>catalysts</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	in cells, regulating metabolism by selectively accelerating chemical
	reactions without being consumed.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Humans have tens of
	thousands of different proteins, each with a specific structure and
	function.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Proteins are the
	most structurally complex molecules known.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each type of
	protein has a complex three-dimensional shape or conformation.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>All protein
	polymers are constructed from the same set of 20 amino acid
	monomers.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Polymers
	of proteins are called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>polypeptides.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>protein</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	consists of one or more polypeptides folded and coiled into a
	specific conformation.</FONT></FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Amino acids
are the monomers from which proteins are constructed.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Amino
	acids</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	are organic molecules with both carboxyl and amino groups.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>At
	the center of an amino acid is an asymmetric carbon atom called the
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>alpha
	carbon.</I></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Four components are
	attached to the alpha carbon: a hydrogen atom, a carboxyl group, an
	amino group, and a variable R group (or side chain).</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Different R groups
	characterize the 20 different amino acids.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>R groups may be as
	simple as a hydrogen atom (as in the amino acid glycine), or it may
	be a carbon skeleton with various functional groups attached (as in
	glutamine).</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The physical and
	chemical properties of the R group determine the unique
	characteristics of a particular amino acid.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>One group of amino
	acids has hydrophobic R groups.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Another group of
	amino acids has polar R groups that are hydrophilic.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A third group of
	amino acids includes those with functional groups that are charged
	(ionized) at cellular pH.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Some acidic R groups
are negative in charge due to the presence of a carboxyl group.</FONT></FONT></P>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Basic R groups have
amino groups that are positive in charge.</FONT></FONT></P>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Note
that </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>all</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
amino acids have carboxyl and amino groups. The terms </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>acidic</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
and </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>basic</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
in this context refer only to these groups in the R groups.</FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Amino acids are
	joined together when a dehydration reaction removes a hydroxyl group
	from the carboxyl end of one amino acid and a hydrogen from the
	amino group of another.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	resulting covalent bond is called a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>peptide
	bond.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Repeating the
	process over and over creates a polypeptide chain.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>At one end is an
	amino acid with a free amino group (the N-terminus) and at the other
	is an amino acid with a free carboxyl group (the C-terminus).</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Polypeptides range
	in size from a few monomers to thousands.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each polypeptide
	has a unique linear sequence of amino acids.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>The amino
acid sequence of a polypeptide can be determined.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Frederick Sanger
	and his colleagues at Cambridge University determined the amino acid
	sequence of insulin in the 1950s.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Sanger used
	protein-digesting enzymes and other catalysts to hydrolyze the
	insulin at specific places.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The fragments were
	then separated by a technique called chromatography.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Hydrolysis by
	another agent broke the polypeptide at different sites, yielding a
	second group of fragments.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Sanger used
	chemical methods to determine the sequence of amino acids in the
	small fragments.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>He then searched
	for overlapping regions among the pieces obtained by hydrolyzing
	with the different agents.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>After years of
	effort, Sanger was able to reconstruct the complete primary
	structure of insulin.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most of the steps
	in sequencing a polypeptide have since been automated.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Protein
conformation determines protein function.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A functional
	protein consists of one or more polypeptides that have been twisted,
	folded, and coiled into a unique shape.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>It is the order of
	amino acids that determines what the three-dimensional conformation
	of the protein will be.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A protein’s
	specific conformation determines its function.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>When a cell
	synthesizes a polypeptide, the chain generally folds spontaneously
	to assume the functional conformation for that protein.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The folding is
	reinforced by a variety of bonds between parts of the chain, which
	in turn depend on the sequence of amino acids.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Many proteins are
	globular, while others are fibrous in shape.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In almost every
	case, the function of a protein depends on its ability to recognize
	and bind to some other molecule.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For example, an
	antibody binds to a particular foreign substance.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>An enzyme
	recognizes and binds to a specific substrate, facilitating a
	chemical reaction.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Natural signal
	molecules called endorphins bind to specific receptor proteins on
	the surface of brain cells in humans, producing euphoria and
	relieving pain.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Morphine, heroin,
and other opiate drugs mimic endorphins because they are similar in
shape and can bind to the brain’s endorphin receptors.</FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The function of a
	protein is an emergent property resulting from its specific
	molecular order.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Three levels of
	structure—primary, secondary, and tertiary structures—organize
	the folding within a single polypeptide.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Quaternary
	structure arises when two or more polypeptides join to form a
	protein.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>primary
	structure</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	of a protein is its unique sequence of amino acids.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Lysozyme, an enzyme
	that attacks bacteria, consists of 129 amino acids.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The precise primary
	structure of a protein is determined by inherited genetic
	information.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Even a slight
	change in primary structure can affect a protein’s conformation
	and ability to function.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	substitution of one amino acid (valine) for the normal one (glutamic
	acid) at a particular position in the primary structure of
	hemoglobin, the protein that carries oxygen in red blood cells, can
	cause </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>sickle-cell
	disease,</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	an inherited blood disorder.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The abnormal
	hemoglobins crystallize, deforming the red blood cells into a sickle
	shape and clogging capillaries.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most proteins have
	segments of their polypeptide chains repeatedly coiled or folded.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These
	coils and folds are referred to as </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>secondary
	structure</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	and result from hydrogen bonds between the repeating constituents of
	the polypeptide backbone.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The weakly positive
	hydrogen atom attached to the nitrogen atom has an affinity for the
	oxygen atom of a nearby peptide bond.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each hydrogen bond
	is weak, but the sum of many hydrogen bonds stabilizes the structure
	of part of the protein.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Typical secondary
	structures are coils (an alpha helix) or folds (beta pleated
	sheets).</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The structural
	properties of silk are due to beta pleated sheets.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The presence of so
	many hydrogen bonds makes each silk fiber stronger than a steel
	strand of the same weight.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Tertiary
	structure</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is determined by interactions among various R groups.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These
	interactions include hydrogen bonds between polar and/or charged
	areas, ionic bonds between charged R groups, and </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>hydrophobic
	interactions</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	and van der Waals interactions among hydrophobic R groups.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>While
	these three interactions are relatively weak, strong covalent bonds
	called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>disulfide
	bridges</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	that form between the sulfhydryl groups (SH) of two cysteine
	monomers act to rivet parts of the protein together.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Quaternary
	structure</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	results from the aggregation of two or more polypeptide subunits.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Collagen is a
	fibrous protein of three polypeptides that are supercoiled like a
	rope.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This provides
structural strength for collagen’s role in connective tissue.</FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Hemoglobin is a
	globular protein with quaternary structure.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>It consists of four
polypeptide subunits: two alpha and two beta chains.</FONT></FONT></P>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Both types of
subunits consist primarily of alpha-helical secondary structure.</FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each subunit has a
	nonpeptide heme component with an iron atom that binds oxygen.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>What are the key
	factors determining protein conformation?</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A polypeptide chain
	of a given amino acid sequence can spontaneously arrange itself into
	a 3D shape determined and maintained by the interactions responsible
	for secondary and tertiary structure.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The folding occurs
	as the protein is being synthesized within the cell.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>However, protein
	conformation also depends on the physical and chemical conditions of
	the protein’s environment.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Alterations
	in pH, salt concentration, temperature, or other factors can unravel
	or </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>denature</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	a protein.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These forces
	disrupt the hydrogen bonds, ionic bonds, and disulfide bridges that
	maintain the protein’s shape.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most proteins
	become denatured if the are transferred to an organic solvent. The
	polypeptide chain refolds so that its hydrophobic regions face
	outward, toward the solvent.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Denaturation can
	also be caused by heat, which disrupts the weak interactions that
	stabilize conformation.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This explains why
	extremely high fevers can be fatal. Proteins in the blood become
	denatured by the high body temperatures.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Some proteins can
	return to their functional shape after denaturation, but others
	cannot, especially in the crowded environment of the cell.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Biochemists now
	know the amino acid sequences of more than 875,000 proteins and the
	3D shapes of about 7,000.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Nevertheless, it is
	still difficult to predict the conformation of a protein from its
	primary structure alone.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most proteins
	appear to undergo several intermediate stages before reaching their
	“mature” configuration.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	folding of many proteins is assisted by </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>chaperonins</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	or chaperone proteins.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Chaperonins do not
	specify the final structure of a polypeptide but rather work to
	segregate and protect the polypeptide while it folds spontaneously.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>At
	present, scientists use </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>X-ray
	crystallography</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	to determine protein conformation.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This technique
	requires the formation of a crystal of the protein being studied.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The pattern of
	diffraction of an X-ray by the atoms of the crystal can be used to
	determine the location of the atoms and to build a computer model of
	its structure.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Nuclear magnetic
	resonance (NMR) spectroscopy has recently been applied to this
	problem.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This method does
	not require protein crystallization.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.17in; margin-bottom: 0in; line-height: 0.17in; text-decoration: none">
<BR>
</P>
<P STYLE="margin-top: 0.17in; margin-bottom: 0in; line-height: 0.17in; text-decoration: none">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Concept
5.5 Nucleic acids store and transmit hereditary information</B></FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	amino acid sequence of a polypeptide is programmed by a unit of
	inheritance known as a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>gene.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A
	gene consists of DNA, a polymer known as a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>nucleic
	acid.</B></FONT></FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>There are two
types of nucleic acids: RNA and DNA.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>There are two types
	of nucleic acids: ribonucleic acid (RNA) and deoxyribonucleic acid
	(DNA).</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These are the
	molecules that allow living organisms to reproduce their complex
	components from generation to generation.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>DNA provides
	directions for its own replication.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>DNA also directs
	RNA synthesis and, through RNA, controls protein synthesis.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Organisms inherit
	DNA from their parents.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each DNA molecule
	is very long, consisting of hundreds to thousands of genes.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Before a cell
	reproduces itself by dividing, its DNA is copied. The copies are
	then passed to the next generation of cells.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>While DNA encodes
	the information that programs all the cell’s activities, it is not
	directly involved in the day-to-day operations of the cell.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Proteins are
	responsible for implementing the instructions contained in DNA.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each
	gene along a DNA molecule directs the synthesis of a specific type
	of </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>messenger
	RNA</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	molecule (mRNA).</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The mRNA molecule
	interacts with the cell’s protein-synthesizing machinery to direct
	the ordering of amino acids in a polypeptide.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The flow of genetic
	information is from DNA -&gt; RNA -&gt; protein.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Protein synthesis
	occurs on cellular structures called ribosomes.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In eukaryotes, DNA
	is located in the nucleus, but most ribosomes are in the cytoplasm.
	mRNA functions as an intermediary, moving information and directions
	from the nucleus to the cytoplasm.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Prokaryotes lack
	nuclei but still use RNA as an intermediary to carry a message from
	DNA to the ribosomes.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>A nucleic
acid strand is a polymer of nucleotides.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Nucleic
	acids are polymers made of </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>nucleotide</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	monomers.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each nucleotide
	consists of three parts: a nitrogenous base, a pentose sugar, and a
	phosphate group.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	nitrogen bases are rings of carbon and nitrogen that come in two
	types: </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>purines</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	and </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>pyrimidines.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Pyrimidines have a
	single six-membered ring.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>There are three
different pyrimidines: cytosine (C), thymine (T), and uracil (U).</FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Purines have a
	six-membered ring joined to a five-membered ring.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The two purines are
adenine (A) and guanine (G).</FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	pentose joined to the nitrogen base is </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>ribose</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	in nucleotides of RNA and </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>deoxyribose</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	in DNA.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The only difference
	between the sugars is the lack of an oxygen atom on carbon two in
	deoxyribose.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Because the atoms
	in both the nitrogenous base and the sugar are numbered, the sugar
	atoms have a prime after the number to distinguish them.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Thus, the second
	carbon in the sugar ring is the 2’ (2 prime) carbon and the carbon
	that sticks up from the ring is the 5’ carbon.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	combination of a pentose and a nitrogenous base is a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>nucleoside.</I></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	addition of a phosphate group creates a nucleoside monophosphate or
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>nucleotide.</I></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Polynucleotides</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	are synthesized when adjacent nucleotides are joined by covalent
	bonds called phosphodiester linkages that form between the —OH
	group on the 3’ of one nucleotide and the phosphate on the 5’
	carbon of the next.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This creates a
	repeating backbone of sugar-phosphate units, with appendages
	consisting of the nitrogenous bases.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The two free ends
	of the polymer are distinct.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>One end has a
	phosphate attached to a 5’ carbon; this is the 5’ end.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The other end has a
	hydroxyl group on a 3’ carbon; this is the 3’ end.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The sequence of
	bases along a DNA or mRNA polymer is unique for each gene.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Because genes are
	normally hundreds to thousands of nucleotides long, the number of
	possible base combinations is virtually limitless.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The linear order of
	bases in a gene specifies the order of amino acids—the primary
	structure—of a protein, which in turn determines three-dimensional
	conformation and function.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Inheritance
is based on replication of the DNA double helix.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>An RNA molecule is
	a single polynucleotide chain.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>DNA
	molecules have two polynucleotide strands that spiral around an
	imaginary axis to form a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>double
	helix.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The double helix
	was first proposed as the structure of DNA in 1953 by James Watson
	and Francis Crick.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The sugar-phosphate
	backbones of the two polynucleotides are on the outside of the
	helix.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	two backbones run in opposite 5’ -&gt; 3’ directions from each
	other, an arrangement referred to as </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>antiparallel.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Pairs of
	nitrogenous bases, one from each strand, connect the polynucleotide
	chains with hydrogen bonds.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most DNA molecules
	have thousands to millions of base pairs.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Because of their
	shapes, only some bases are compatible with each other.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Adenine (A) always
	pairs with thymine (T) and guanine (G) with cytosine (C).</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>With these
	base-pairing rules, if we know the sequence of bases on one strand,
	we know the sequence on the opposite strand.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	two strands are </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>complementary.</I></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Prior to cell
	division, each of the strands serves as a template to order
	nucleotides into a new complementary strand.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This results in two
	identical copies of the original double-stranded DNA molecule, which
	are then distributed to the daughter cells.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This mechanism
	ensures that a full set of genetic information is transmitted
	whenever a cell reproduces.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>We can use DNA
and proteins as tape measures of evolution.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Genes (DNA) and
	their products (proteins) document the hereditary background of an
	organism.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Because DNA
	molecules are passed from parents to offspring, siblings have
	greater similarity in their DNA and protein than do unrelated
	individuals of the same species.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This
	argument can be extended to develop a “molecular genealogy” to
	relationships </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>between</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	species.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Two species that
	appear to be closely related based on fossil and molecular evidence
	should also be more similar in DNA and protein sequences than are
	more distantly related species.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In fact, that is
	so.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For example, if we
compare the sequence of 146 amino acids in a hemoglobin polypeptide,
we find that humans and gorillas differ in just 1 amino acid.</FONT></FONT></P>
<UL>
	<UL>
		<UL>
			<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
			<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Humans and
			gibbons differ in 2 amino acids.</FONT></FONT></P>
			<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
			<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Humans and rhesus
			monkeys differ in 8 amino acids.</FONT></FONT></P>
		</UL>
	</UL>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>More distantly
related species have more differences.</FONT></FONT></P>
<UL>
	<UL>
		<UL>
			<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
			<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Humans and mice
			differ in 27 amino acids.</FONT></FONT></P>
			<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
			<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Humans and frogs
			differ in 67 amino acids.</FONT></FONT></P>
		</UL>
	</UL>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Molecular biology
can be used to assess evolutionary kinship.</FONT></FONT></P>
