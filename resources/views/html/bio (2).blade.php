<P ALIGN=CENTER STYLE="margin-bottom: 0in; text-decoration: none"><FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Chapter
3</B></FONT></FONT></FONT></P>
<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT COLOR="#ff0000"><SPAN STYLE="text-decoration: none">
   </SPAN></FONT><FONT SIZE=4 STYLE="font-size: 16pt"><B><FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><SPAN STYLE="text-decoration: none">Water
and the Fitness of the Environment</SPAN></FONT></FONT></FONT></B></FONT></P>
<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Lecture
Outline</B></I></FONT></FONT></P>
<P STYLE="margin-top: 0.22in; margin-bottom: 0.17in; text-decoration: none">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Overview:
The Molecule That Supports All of Life</B></FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Because
	water is the substance that makes life possible on Earth,
	astronomers hope to find evidence of water on newly discovered
	planets orbiting distant stars.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Life on Earth began
	in water and evolved there for 3 billion years before colonizing the
	land.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Even terrestrial
	organisms are tied to water.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most cells are
	surrounded by water.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cells are about
	70–95% water.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water
	is a reactant in many of the chemical reactions of life.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water is the only
	common substance that exists in the natural world in all three
	physical states of matter: solid ice, liquid water, and water vapor.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
<BR>
</P>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
<FONT SIZE=2 STYLE="font-size: 11pt"><B><FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><SPAN STYLE="font-style: normal"><SPAN STYLE="text-decoration: none">Concept
3.1 The polarity of water molecules results in hydrogen bonding</SPAN></SPAN></FONT></FONT></FONT></B></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In a water
	molecule, two hydrogen atoms form single polar covalent bonds with
	an oxygen atom.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Because oxygen is
	more electronegative than hydrogen, the region around the oxygen
	atom has a partial negative charge.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	regions near the two hydrogen atoms have a partial positive charge.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A water molecule is
	a polar molecule in which opposite ends of the molecule have
	opposite charges.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water has a variety
	of unusual properties because of the attraction between polar water
	molecules.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	slightly negative regions of one water molecule are attracted to the
	slightly positive regions of nearby water molecules, forming
	hydrogen bonds.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each water molecule
	can form hydrogen bonds with up to four neighbors.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<BR>
</P>
<P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT SIZE=2 STYLE="font-size: 11pt"><FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><SPAN STYLE="text-decoration: none"><B>Concept
3.2 Four emergent properties of water contribute to Earth’s fitness
for life</B></SPAN></FONT></FONT></FONT></FONT></P>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Organisms
depend on the cohesion of water molecules.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The hydrogen bonds
	joining water molecules are weak, about 1/20 as strong as covalent
	bonds.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>They
	form, break, and reform with great frequency. Each hydrogen bond
	lasts only a few trillionths of a second.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>At any instant, a
	substantial percentage of all water molecules are bonded to their
	neighbors, creating a high level of structure.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Collectively,
	hydrogen bonds hold water together, a phenomenon called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>cohesion.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cohesion
	among water molecules plays a key role in the transport of water and
	dissolved nutrients against gravity in plants.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water molecules
	move from the roots to the leaves of a plant through
	water-conducting vessels.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>As
	water molecules evaporate from a leaf, other water molecules from
	vessels in the leaf replace them.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Hydrogen bonds
	cause water molecules leaving the vessels to tug on molecules
	farther down.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This upward pull is
	transmitted down to the roots.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Adhesion,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	clinging of one substance to another, contributes too, as water
	adheres to the wall of the vessels.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Surface
	tension,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	a measure of the force necessary to stretch or break the surface of
	a liquid, is related to cohesion.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water
	has a greater surface tension than most other liquids because
	hydrogen bonds among surface water molecules resist stretching or
	breaking the surface.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water behaves as if
	covered by an invisible film.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Some animals can
	stand, walk, or run on water without breaking the surface.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Water
moderates temperatures on Earth.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water
	stabilizes air temperatures by absorbing heat from warmer air and
	releasing heat to cooler air.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water can absorb or
	release relatively large amounts of heat with only a slight change
	in its own temperature.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Atoms
	and molecules have </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>kinetic
	energy,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	the energy of motion, because they are always moving.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The faster a
	molecule moves, the more kinetic energy it has.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Heat</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is a measure of the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>total</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	quantity of kinetic energy due to molecular motion in a body of
	matter.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Temperature</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	measures the intensity of heat in a body of matter due to the
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>average</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	kinetic energy of molecules.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>As the average
	speed of molecules increases, a thermometer will record an increase
	in temperature.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Heat and
	temperature are related, but not identical.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>When
	two objects of different temperatures come together, heat passes
	from the warmer object to the cooler object until the two are the
	same temperature.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Molecules in the
	cooler object speed up at the expense of kinetic energy of the
	warmer object.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Ice
	cubes cool a glass of pop by absorbing heat from the pop as the ice
	melts.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	most biological settings, temperature is measured on the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Celsius
	scale</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	(°C).</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>At sea level, water
	freezes at 0°C and boils at 100°C.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Human body
	temperature is typically 37°C.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>While
	there are several ways to measure heat energy, one convenient unit
	is the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>calorie
	(cal).</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>One calorie is the
	amount of heat energy necessary to raise the temperature of one g of
	water by 1°C.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A calorie is
	released when 1 g of water cools by 1°C.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	many biological processes, the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>kilocalorie
	(kcal)</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is more convenient.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A kilocalorie is
	the amount of heat energy necessary to raise the temperature of 1000
	g of water by 1°C.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Another
	common energy unit, the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>joule
	(J),</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is equivalent to 0.239 cal.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water stabilizes
	temperature because it has a high specific heat.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>specific
	heat</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	of a substance is the amount of heat that must be absorbed or lost
	for 1 g of that substance to change its temperature by 1°C.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>By definition, the
	specific heat of water is 1 cal per gram per degree Celsius or 1
	cal/g/°C.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water
	has a high specific heat compared to other substances.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For example, ethyl
	alcohol has a specific heat of 0.6 cal/g/°C.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The specific heat
	of iron is 1/10 that of water.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water resists
	changes in temperature because of its high specific heat.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	other words, water absorbs or releases a relatively large quantity
	of heat for each degree of temperature change.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water’s high
	specific heat is due to hydrogen bonding.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Heat must be
	absorbed to break hydrogen bonds, and heat is released when hydrogen
	bonds form.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Investment
	of one calorie of heat causes relatively little change to the
	temperature of water because much of the energy is used to disrupt
	hydrogen bonds, not speed up the movement of water molecules.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water’s
	high specific heat has effects that range from the level of the
	whole Earth to the level of individual organisms.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A large body of
	water can absorb a large amount of heat from the sun in daytime
	during the summer and yet warm only a few degrees.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>At night and during
	the winter, the warm water will warm cooler air.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Therefore,
	ocean temperatures and coastal land areas have more stable
	temperatures than inland areas.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Living things are
	made primarily of water. Consequently, they resist changes in
	temperature better than they would if composed of a liquid with a
	lower specific heat.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	transformation of a molecule from a liquid to a gas is called
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>vaporization</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	or </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>evaporation.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This occurs when
	the molecule moves fast enough to overcome the attraction of other
	molecules in the liquid.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Even
	in a low-temperature liquid (with low average kinetic energy), some
	molecules are moving fast enough to evaporate.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Heating a liquid
	increases the average kinetic energy and increases the rate of
	evaporation.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Heat
	of vaporization</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is the quantity of heat that a liquid must absorb for 1 g of it to
	be converted from liquid to gas.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water has a
	relatively high heat of vaporization, requiring about 580 cal of
	heat to evaporate 1 g of water at room temperature.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This is double the
	heat required to vaporize the same quantity of alcohol or ammonia.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This
	is because hydrogen bonds must be broken before a water molecule can
	evaporate from the liquid.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water’s high heat
	of vaporization moderates climate.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Much of the sun’s
	heat absorbed by tropical oceans is used for evaporation of surface
	water.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>As
	moist tropical air moves to the poles, water vapor condenses to form
	rain, releasing heat.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>As
	a liquid evaporates, the surface of the liquid that remains behind
	cools, a phenomenon called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>evaporative
	cooling.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This
	occurs because the most energetic molecules are the most likely to
	evaporate, leaving the lower–kinetic energy molecules behind.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Evaporative cooling
	moderates temperature in lakes and ponds.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Evaporation of
	sweat in mammals or evaporation of water from the leaves of plants
	prevents terrestrial organisms from overheating.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Evaporation of
	water from the leaves of plants or the skin of humans removes excess
	heat.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Oceans and
lakes don’t freeze solid because ice floats.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water is unusual
	because it is less dense as a solid than as a cold liquid.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most
	materials contract as they solidify, but water expands.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>At temperatures
	above 4°C, water behaves like other liquids, expanding as it warms
	and contracting as it cools.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water begins to
	freeze when its molecules are no longer moving vigorously enough to
	break their hydrogen bonds.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>When
	water reaches 0°C, water becomes locked into a crystalline lattice,
	with each water molecule bonded to a maximum of four partners.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>As
	ice starts to melt, some of the hydrogen bonds break, and water
	molecules can slip closer together than they can while in the ice
	state.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Ice is about 10%
	less dense than water at 4°C.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Therefore, ice
	floats on the cool water below.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This oddity has
	important consequences for life.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>If ice sank,
	eventually all ponds, lakes, and even the ocean would freeze solid.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>During
	the summer, only the upper few centimeters of the ocean would thaw.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Instead, the
	surface layer of ice insulates liquid water below, preventing it
	from freezing and allowing life to exist under the frozen surface.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Water is the
solvent of life.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A
	liquid that is a completely homogeneous mixture of two or more
	substances is called a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>solution.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A sugar cube in a
	glass of water will eventually dissolve to form a uniform solution
	of sugar and water.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	dissolving agent is the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>solvent,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	and the substance that is dissolved is the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>solute.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In our example,
	water is the solvent and sugar is the solute.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	an </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>aqueous
	solution,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	water is the solvent.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water is not a
	universal solvent, but it is very versatile because of the polarity
	of water molecules.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water
	is an effective solvent because it readily forms hydrogen bonds with
	charged and polar covalent molecules.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For
	example, when a crystal of salt (NaCl) is placed in water, the Na</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>+</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	cations interact with the partial negative charges of the oxygen
	regions of water molecules.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	Cl</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>−</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	anions interact with the partial positive charges of the hydrogen
	regions of water molecules.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each
	dissolved ion is surrounded by a sphere of water molecules, a
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>hydration
	shell.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Eventually,
	water dissolves all the ions, resulting in a solution with two
	solutes: sodium and chloride ions.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Polar molecules are
	also soluble in water because they form hydrogen bonds with water.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Even large
	molecules, like proteins, can dissolve in water if they have ionic
	and polar regions.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Any
	substance that has an affinity for water is </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>hydrophilic</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>(water-loving).</I></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These substances
	are dominated by ionic or polar bonds.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Some hydrophilic
	substances do not dissolve because their molecules are too large.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For
	example, cotton is hydrophilic because cellulose, its major
	constituent, has numerous polar covalent bonds. However, its giant
	cellulose molecules are too large to dissolve in water.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water
	molecules form hydrogen bonds with the cellulose fibers of cotton,
	allowing you to dry yourself with your cotton towel as the water is
	pulled into the towel.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Substances
	that have no affinity for water are </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>hydrophobic</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>(water-fearing).</I></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These substances
	are nonionic and have nonpolar covalent bonds.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Because
	there are no consistent regions with partial or full charges, water
	molecules cannot form hydrogen bonds with hydrophobic molecules.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Oils such as
	vegetable oil are hydrophobic because the dominant bonds,
	carbon-carbon and carbon-hydrogen, share electrons equally.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Hydrophobic
	molecules are major ingredients of cell membranes.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Biological
	chemistry is “wet” chemistry with most reactions involving
	solutes dissolved in water.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Chemical reactions
	depend on collisions of molecules and therefore on the
	concentrations of solutes in aqueous solution.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>We
	measure the number of molecules in units called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>moles.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	actual number of molecules in a mole is called Avogadro’s number,
	6.02 × 10</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>23</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A mole is equal to
	the molecular weight of a substance but scaled up from daltons to
	grams.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>To
	illustrate, how could we measure out a mole of table sugar—sucrose
	(C</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>12</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>H</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>22</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>O</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>11</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>)?</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A
	carbon atom weighs 12 daltons, hydrogen 1 dalton, and oxygen 16
	daltons.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>One
	molecule of sucrose would weigh 342 daltons, the sum of weights of
	all the atoms in sucrose, or the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>molecular
	weight</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	of sucrose.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>To
	get one mole of sucrose, we would weigh out 342 g.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The advantage of
	using moles as a measurement is that a mole of one substance has the
	same number of molecules as a mole of any other substance.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>If
	substance A has a molecular weight of 10 daltons and substance B has
	a molecular weight of 100 daltons, then we know that 10 g of
	substance A has the same number of molecules as 100 g of substance
	B.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A
	mole of sucrose contains 6.02 × 10</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>23</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	molecules and weighs 342 g, while a mole of ethyl alcohol (C</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>H</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>6</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>O)
	also contains 6.02 × 10</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>23</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	molecules but weighs only 46 g because the molecules are smaller.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Measuring in moles
	allows scientists to combine substances in fixed ratios of
	molecules.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In “wet”
	chemistry, we are typically combining solutions or measuring the
	quantities of materials in aqueous solutions.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	concentration of a material in solution is called its </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>molarity.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A one molar
	solution has one mole of a substance dissolved in one liter of
	solvent, typically water.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>To
	make a 1 molar (1M) solution of sucrose, we would slowly add water
	to 342 g of sucrose until the total volume was 1 liter and all the
	sugar was dissolved.</FONT></FONT></FONT></P>
</UL>
