	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	water molecule that lost the proton is now a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>hydroxide
	ion</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	(OH</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>−</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>).</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	water molecule with the extra proton is now a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>hydronium
	ion</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	(H</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>3</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>O</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>+</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>).</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A simplified way to
	view this process is to say that a water molecule dissociates into a
	hydrogen ion and a hydroxide ion:</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>H</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>O
	&lt;=&gt; H</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>+</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	+ OH</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>−</FONT></FONT></SUP></FONT></P>
