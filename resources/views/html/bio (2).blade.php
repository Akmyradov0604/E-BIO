<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Occasionally,
