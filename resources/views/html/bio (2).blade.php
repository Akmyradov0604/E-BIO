	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	H</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>+</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	and OH</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>−</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	concentrations of solutions can vary by a factor of 100 trillion or
	more.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>To
	express this variation more conveniently, the H</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>+</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	and OH</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>−</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	concentrations are typically expressed via the pH scale.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The pH scale,
	ranging from 1 to 14, compresses the range of concentrations by
	employing logarithms. </FONT></FONT>
	</P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>pH
	= − log [H</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>+</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>]
	or [H</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>+</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>]
	= 10</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>−pH</FONT></FONT></SUP></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	a neutral solution, [H+] = 10−7 M, and the pH = 7.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Values
	for pH </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>decline</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	as [H</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>+</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>]
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>increase</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>While
	the pH scale is based on [H</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>+</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>],
	values for [OH</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>−</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>]
	can be easily calculated from the product relationship.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The pH of a neutral
	solution is 7.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Acidic
	solutions have pH values less than 7, and basic solutions have pH
	values greater than 7.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most biological
	fluids have pH values in the range of 6 to 8.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>However, the human
	stomach has strongly acidic digestive juice with a pH of about 2.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each
	pH unit represents a tenfold difference in H</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>+</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	and OH</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>−</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	concentrations.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A
	small change in pH actually indicates a substantial change in H</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>+</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	and OH</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>−</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	concentrations.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	chemical processes in the cell can be disrupted by changes to the H</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>+</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	and OH</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>−</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	concentrations away from their normal values, usually near pH 7.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>To
	maintain cellular pH values at a constant level, biological fluids
	have </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>buffers.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Buffers
	resist changes to the pH of a solution when H</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>+</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	or OH</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>−</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is added to the solution.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Buffers
	accept hydrogen ions from the solution when they are in excess and
	donate hydrogen ions when they have been depleted.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Buffers typically
	consist of a weak acid and its corresponding base.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>One
	important buffer in human blood and other biological solutions is
	carbonic acid, which dissociates to yield a bicarbonate ion and a
	hydrogen ion.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	chemical equilibrium between carbonic acid and bicarbonate acts as a
	pH regulator. The equilibrium shifts left or right as other
	metabolic processes add or remove H</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>+</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	from the solution.</FONT></FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Acid
precipitation threatens the fitness of the environment.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Acid
	precipitation</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is a serious assault on water quality in some industrialized areas.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Uncontaminated rain
	has a slightly acidic pH of 5.6.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The acid is a
	product of the formation of carbonic acid from carbon dioxide and
	water.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Acid
	precipitation occurs when rain, snow, or fog has a pH that is more
	acidic than 5.6.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Acid precipitation
	is caused primarily by sulfur oxides and nitrogen oxides in the
	atmosphere.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These molecules
	react with water to form strong acids that fall to the surface with
	rain or snow.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	major source of these oxides is the burning of fossil fuels (coal,
	oil, and gas) in factories and automobiles.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	presence of tall smokestacks allows this pollution to spread from
	its site of origin to contaminate relatively pristine areas
	thousands of kilometers away.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In 2001, rain in
	the Adirondack Mountains of upstate New York had an average pH of
	4.3.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The effects of
	acids in lakes and streams are more pronounced in the spring during
	snowmelt.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>As
	the surface snows melt and drain down through the snowfield, the
	meltwater accumulates acid and brings it into lakes and streams all
	at once.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The pH of early
	meltwater may be as low as 3.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Acid
	precipitation has a great impact on the eggs and the early
	developmental stages of aquatic organisms that are abundant in the
	spring.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Thus, strong
	acidity can alter the structure of molecules and impact ecological
	communities.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Direct impacts of
	acid precipitation on forests and terrestrial life are more
	controversial.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>However,
	acid precipitation can impact soils by affecting the solubility of
	soil minerals.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Acid precipitation
	can wash away key soil buffers and plant nutrients such as calcium
	and magnesium ions.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>It can also
	increase the concentrations of compounds such as aluminum to toxic
	levels.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This
	has done major damage to forests in Europe and substantial damage of
	forests in North America.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Progress has been
	made in reducing acid precipitation.</FONT></FONT></P>
</UL>
