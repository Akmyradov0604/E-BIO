	intermembrane space, the stroma, and the thylakoid space.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Like mitochondria,
	chloroplasts are dynamic structures.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Their shape is
	plastic, and they can reproduce themselves by pinching in two.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Mitochondria and
	chloroplasts are mobile and move around the cell along tracks of the
