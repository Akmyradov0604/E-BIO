<P STYLE="margin-top: 0.17in; margin-bottom: 0in; line-height: 0.17in; text-decoration: none">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Concept
6.6 The cytoskeleton is a network of fibers that organizes structures
and activities in the cell</B></FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>cytoskeleton</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is a network of fibers extending throughout the cytoplasm.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The cytoskeleton
	organizes the structures and activities of the cell.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>The
cytoskeleton provides support, motility, and regulation.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>cytoskeleton</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	provides mechanical support and maintains cell shape.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The cytoskeleton
	provides anchorage for many organelles and cytosolic enzymes.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The cytoskeleton is
	dynamic and can be dismantled in one part and reassembled in another
	to change the shape of the cell.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The cytoskeleton
	also plays a major role in cell motility, including changes in cell
	location and limited movements of parts of the cell.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The cytoskeleton
	interacts with motor proteins to produce motility.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cytoskeleton
	elements and motor proteins work together with plasma membrane
	molecules to move the whole cell along fibers outside the cell.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Motor proteins
	bring about movements of cilia and flagella by gripping cytoskeletal
	components such as microtubules and moving them past each other.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The same mechanism
	causes muscle cells to contract.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Inside the cell,
	vesicles can travel along “monorails” provided by the
	cytoskeleton.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The cytoskeleton
	manipulates the plasma membrane to form food vacuoles during
	phagocytosis.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cytoplasmic
	streaming in plant cells is caused by the cytoskeleton.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Recently, evidence
	suggests that the cytoskeleton may play a role in the regulation of
	biochemical activities in the cell.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>There
	are three main types of fibers making up the cytoskeleton:
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>microtubules,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>microfilaments,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	and </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>intermediate
	filaments.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Microtubules,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	the thickest fibers, are hollow rods about 25 microns in diameter
	and 200 nm to 25 microns in length.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Microtubule fibers
	are constructed of the globular protein tubulin.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each tubulin
	molecule is a dimer consisting of two subunits.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A microtubule
	changes in length by adding or removing tubulin dimers.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Microtubules shape
	and support the cell and serve as tracks to guide motor proteins
	carrying organelles to their destination.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Microtubules are
	also responsible for the separation of chromosomes during cell
	division.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	many cells, microtubules grow out from a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>centrosome</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	near the nucleus.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These microtubules
	resist compression to the cell.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	animal cells, the centrosome has a pair of </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>centrioles</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>,
	each with nine triplets of microtubules arranged in a ring.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Before a cell
	divides, the centrioles replicate.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A
	specialized arrangement of microtubules is responsible for the
	beating of </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>cilia</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	and </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>flagella.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Many unicellular
	eukaryotic organisms are propelled through water by cilia and
	flagella.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cilia or flagella
	can extend from cells within a tissue layer, beating to move fluid
	over the surface of the tissue.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For example, cilia
lining the windpipe sweep mucus carrying trapped debris out of the
lungs.</FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cilia usually occur
	in large numbers on the cell surface.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>They are about 0.25
	microns in diameter and 2–20 microns long.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>There are usually
	just one or a few flagella per cell.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Flagella are the
	same width as cilia, but 10–200 microns long.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cilia and flagella
	differ in their beating patterns.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A flagellum has an
	undulatory movement that generates force in the same direction as
	the flagellum’s axis.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cilia move more
	like oars with alternating power and recovery strokes that generate
	force perpendicular to the cilium’s axis.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In spite of their
	differences, both cilia and flagella have the same ultrastructure.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Both have a core of
	microtubules sheathed by the plasma membrane.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Nine doublets of
	microtubules are arranged in a ring around a pair at the center.
	This “9 + 2” pattern is found in nearly all eukaryotic cilia and
	flagella.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Flexible “wheels”
	of proteins connect outer doublets to each other and to the two
	central microtubules.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The outer doublets
	are also connected by motor proteins.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	cilium or flagellum is anchored in the cell by a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>basal
	body,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	whose structure is identical to a centriole.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	bending of cilia and flagella is driven by the arms of a motor
	protein, </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>dynein.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Addition and
	removal of a phosphate group causes conformation changes in dynein.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Dynein arms
	alternately grab, move, and release the outer microtubules.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Protein cross-links
	limit sliding. As a result, the forces exerted by the dynein arms
	cause the doublets to curve, bending the cilium or flagellum.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Microfilaments</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	are solid rods about 7 nm in diameter.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each microfilament
	is built as a twisted double chain of actin subunits.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Microfilaments can
	form structural networks due to their ability to branch.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The structural role
	of microfilaments in the cytoskeleton is to bear tension, resisting
	pulling forces within the cell.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>They form a
	three-dimensional network just inside the plasma membrane to help
	support the cell’s shape, giving the cell cortex the semisolid
	consistency of a gel.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Microfilaments are
	important in cell motility, especially as part of the contractile
	apparatus of muscle cells.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In muscle cells,
	thousands of actin filaments are arranged parallel to one another.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Thicker
	filaments composed of </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>myosin</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	interdigitate with the thinner actin fibers.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Myosin molecules
	act as motor proteins, walking along the actin filaments to shorten
	the cell.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In other cells,
	actin-myosin aggregates are less organized but still cause localized
	contraction.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A contracting belt
	of microfilaments divides the cytoplasm of animal cells during cell
	division.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Localized
	contraction brought about by actin and myosin also drives amoeboid
	movement.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Pseudopodia,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
cellular extensions, extend and contract through the reversible
assembly and contraction of actin subunits into microfilaments.</FONT></FONT></FONT></P>
<UL>
	<UL>
		<UL>
			<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
			<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Microfilaments
			assemble into networks that convert sol to gel.</FONT></FONT></P>
			<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
			<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>According to a
			widely accepted model, filaments near the cell’s trailing edge
			interact with myosin, causing contraction.</FONT></FONT></P>
			<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
			<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The contraction
			forces the interior fluid into the pseudopodium, where the actin
			network has been weakened.</FONT></FONT></P>
			<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
			<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The pseudopodium
			extends until the actin reassembles into a network.</FONT></FONT></P>
		</UL>
	</UL>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	plant cells, actin-myosin interactions and sol-gel transformations
	drive </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>cytoplasmic
	streaming.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This creates a
	circular flow of cytoplasm in the cell, speeding the distribution of
	materials within the cell.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Intermediate
	filaments</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	range in diameter from 8–12 nanometers, larger than microfilaments
	but smaller than microtubules.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Intermediate
	filaments are a diverse class of cytoskeletal units, built from a
	family of proteins called keratins.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Intermediate
	filaments are specialized for bearing tension.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Intermediate
	filaments are more permanent fixtures of the cytoskeleton than are
	the other two classes.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>They reinforce cell
	shape and fix organelle location.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.17in; margin-bottom: 0in; line-height: 0.17in; text-decoration: none">
<BR>
</P>
<P STYLE="margin-top: 0.17in; margin-bottom: 0in; line-height: 0.17in; text-decoration: none">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Concept
6.7 Extracellular components and connections between cells help
coordinate cellular activities</B></FONT></FONT></FONT></P>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Plant cells
are encased by cell walls.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>cell
	wall,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	found in prokaryotes, fungi, and some protists, has multiple
	functions.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In plants, the cell
	wall protects the cell, maintains its shape, and prevents excessive
	uptake of water.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>It also supports
	the plant against the force of gravity.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The thickness and
	chemical composition of cell walls differs from species to species
	and among cell types within a plant.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The basic design
	consists of microfibrils of cellulose embedded in a matrix of
	proteins and other polysaccharides. This is the basic design of
	steel-reinforced concrete or fiberglass.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A
	mature cell wall consists of a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>primary
	cell wall, a middle lamella</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	with sticky polysaccharides that holds cells together, and layers of
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>secondary
	cell wall.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Plant cell walls
	are perforated by channels between adjacent cells called
	plasmodesmata.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>The
extracellular matrix (ECM) of animal cells functions in support,
adhesion, movement, and regulation.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Though
	lacking cell walls, animal cells do have an elaborate </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>extracellular
	matrix (ECM).</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	primary constituents of the extracellular matrix are glycoproteins,
	especially </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>collagen</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	fibers, embedded in a network of glycoprotein </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>proteoglycans</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	many cells, </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>fibronectins</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	in the ECM connect to </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>integrins,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	intrinsic membrane proteins that span the membrane and bind on their
	cytoplasmic side to proteins attached to microfilaments of the
	cytoskeleton.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	interconnections from the ECM to the cytoskeleton via the
	fibronectin-integrin link permit the integration of changes inside
	and outside the cell.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The ECM can
	regulate cell behavior.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Embryonic cells
	migrate along specific pathways by matching the orientation of their
	microfilaments to the “grain” of fibers in the extracellular
	matrix.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The extracellular
	matrix can influence the activity of genes in the nucleus via a
	combination of chemical and mechanical signaling pathways.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This may coordinate
the behavior of all the cells within a tissue.</FONT></FONT></P>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Intercellular
junctions help integrate cells into higher levels of structure and
function.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Neighboring cells
	in tissues, organs, or organ systems often adhere, interact, and
	communicate through direct physical contact.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Plant
	cells are perforated with </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>plasmodesmata,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	channels allowing cytosol to pass between cells.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water and small
	solutes can pass freely from cell to cell.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In certain
	circumstances, proteins and RNA can be exchanged.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Animals have 3 main
	types of intercellular links: tight junctions, desmosomes, and gap
	junctions.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>tight
	junctions,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	membranes of adjacent cells are fused, forming continuous belts
	around cells.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This prevents
	leakage of extracellular fluid.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Desmosomes</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	(or anchoring junctions) fasten cells together into strong sheets,
	much like rivets.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Intermediate
	filaments of keratin reinforce desmosomes.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Gap
	junctions</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	(or communicating junctions) provide cytoplasmic channels between
	adjacent cells.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Special membrane
	proteins surround these pores.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Ions, sugars, amino
	acids, and other small molecules can pass.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In embryos, gap
	junctions facilitate chemical communication during development.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>A cell is a
living unit greater than the sum of its parts.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>While the cell has
	many structures with specific functions, all these structures must
	work together.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For example,
	macrophages use actin filaments to move and extend pseudopodia to
	capture their bacterial prey.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Food vacuoles are
	digested by lysosomes, a product of the endomembrane system of ER
	and Golgi.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The enzymes of the
	lysosomes and proteins of the cytoskeleton are synthesized on the
	ribosomes.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The information for
	the proteins comes from genetic messages sent by DNA in the nucleus.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>All of these
	processes require energy in the form of ATP, most of which is
	supplied by the mitochondria.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A cell is a living
	unit greater than the sum of its parts.</FONT></FONT></P>
</UL>
