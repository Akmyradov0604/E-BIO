<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Lecture
Outline</B></I></FONT></FONT></P>
<P STYLE="margin-top: 0.22in; margin-bottom: 0.17in; text-decoration: none">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Overview:
The Importance of Cells</B></FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Times, serif"><FONT SIZE=2 STYLE="font-size: 11pt"><B><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>All
	organisms are made of cells.</FONT></FONT></B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Many organisms are
	single-celled.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Even in
	multicellular organisms, the cell is the basic unit of structure and
	function.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The cell is the
	simplest collection of matter that can live.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>All cells are
	related by their descent from earlier cells.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; font-style: normal; line-height: 0.17in">
<BR>
</P>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; font-style: normal; line-height: 0.17in">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Concept
6.1 To study cells, biologists use microscopes and the tools of
biochemistry</B></FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	discovery and early study of cells progressed with the invention of
	microscopes in 1590 and their improvement in the 17</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>th</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	century.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In a light
	microscope (LM), visible light passes through the specimen and then
	through glass lenses.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The lenses refract
	light such that the image is magnified into the eye or onto a video
	screen.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Microscopes vary in
	magnification and resolving power.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Magnification</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is the ratio of an object’s image to its real size.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Resolving
	power</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is a measure of image clarity.</FONT></FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>It is the minimum
distance two points can be separated and still be distinguished as
two separate points.</FONT></FONT></P>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Resolution is
limited by the shortest wavelength of the radiation used for imaging.</FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The minimum
	resolution of a light microscope is about 200 nanometers (nm), the
	size of a small bacterium.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Light microscopes
	can magnify effectively to about 1,000 times the size of the actual
	specimen.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>At higher
	magnifications, the image blurs.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Techniques
	developed in the 20th century have enhanced contrast and enabled
	particular cell components to be stained or labeled so they stand
	out.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>While
	a light microscope can resolve individual cells, it cannot resolve
	much of the internal anatomy, especially the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>organelles.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>To
	resolve smaller structures, we use an </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>electron
	microscope (EM),</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	which focuses a beam of electrons through the specimen or onto its
	surface.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Because resolution
	is inversely related to wavelength used, electron microscopes (whose
	electron beams have shorter wavelengths than visible light) have
	finer resolution.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Theoretically, the
	resolution of a modern EM could reach 0.002 nanometer (nm), but the
	practical limit is closer to about 2 nm.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Transmission
	electron microscopes (TEMs)</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	are used mainly to study the internal ultrastructure of cells.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A TEM aims an
	electron beam through a thin section of the specimen.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The image is
	focused and magnified by electromagnets.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>To enhance
	contrast, the thin sections are stained with atoms of heavy metals.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Scanning
	electron microscopes (SEMs)</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	are useful for studying surface structures.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The sample surface
	is covered with a thin film of gold.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The beam excites
	electrons on the surface of the sample.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These secondary
	electrons are collected and focused on a screen.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The result is an
	image of the topography of the specimen.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The SEM has great
	depth of field, resulting in an image that seems three-dimensional.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Electron
	microscopes reveal organelles that are impossible to resolve with
	the light microscope.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>However, electron
	microscopes can only be used on dead cells.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Light microscopes
	do not have as high a resolution, but they can be used to study live
	cells.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Microscopes
	are major tools in </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>cytology,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	the study of cell structures.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cytology combined
	with biochemistry, the study of molecules and chemical processes in
	metabolism, to produce modern cell biology.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Cell
biologists can isolate organelles to study their functions.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	goal of </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>cell
	fractionation</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is to separate the major organelles of the cells so their individual
	functions can be studied.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This
	process is driven by an </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>ultracentrifuge,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	a machine that can spin at up to 130,000 revolutions per minute and
	apply forces of more than 1 million times gravity (1,000,000 g).</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Fractionation
	begins with homogenization, gently disrupting the cell.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The homogenate is
	spun in a centrifuge to separate heavier pieces into the pellet
	while lighter particles remain in the supernatant.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>As the process is
	repeated at higher speeds and for longer durations, smaller and
	smaller organelles can be collected in subsequent pellets.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cell fractionation
	prepares isolates of specific cell components.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This enables the
	functions of these organelles to be determined, especially by the
	reactions or processes catalyzed by their proteins.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For example, one
	cellular fraction was enriched in enzymes that function in cellular
	respiration.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Electron microscopy
	revealed that this fraction is rich in mitochondria.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This evidence
	helped cell biologists determine that mitochondria are the site of
	cellular respiration.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cytology and
	biochemistry complement each other in correlating cellular structure
	and function.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.17in; margin-bottom: 0in; line-height: 0.17in; text-decoration: none">
<BR>
</P>
<P STYLE="margin-top: 0.17in; margin-bottom: 0in; line-height: 0.17in; text-decoration: none">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Concept
6.2 Eukaryotic cells have internal membranes that compartmentalize
their functions</B></FONT></FONT></FONT></P>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Prokaryotic
and eukaryotic cells differ in size and complexity.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>All
	cells are surrounded by a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>plasma
	membrane.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	semifluid substance within the membrane is the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>cytosol,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	containing the organelles.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>All
	cells contain </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>chromosomes</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	that have genes in the form of DNA.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>All
	cells also have </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>ribosomes,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	tiny organelles that make proteins using the instructions contained
	in genes.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A major difference
	between prokaryotic and eukaryotic cells is the location of
	chromosomes.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	a eukaryotic cell, chromosomes are contained in a membrane-enclosed
	organelle, the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>nucleus.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>prokaryotic
	cell,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	the DNA is concentrated in the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>nucleoid</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	without a membrane separating it from the rest of the cell.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>eukaryote
	cells,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	the chromosomes are contained within a membranous nuclear envelope.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	region between the nucleus and the plasma membrane is the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>cytoplasm.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>All the material
	within the plasma membrane of a prokaryotic cell is cytoplasm.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Within the
	cytoplasm of a eukaryotic cell are a variety of membrane-bound
	organelles of specialized form and function.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These
	membrane-bound organelles are absent in prokaryotes.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Eukaryotic cells
	are generally much bigger than prokaryotic cells.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The logistics of
	carrying out metabolism set limits on cell size.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>At the lower limit,
	the smallest bacteria, mycoplasmas, are between 0.1 to 1.0 micron.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most bacteria are
	1–10 microns in diameter.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Eukaryotic cells
	are typically 10–100 microns in diameter.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Metabolic
	requirements also set an upper limit to the size of a single cell.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>As a cell increases
	in size, its volume increases faster than its surface area.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Smaller objects
	have a greater ratio of surface area to volume.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>plasma
	membrane</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	functions as a selective barrier that allows the passage of oxygen,
	nutrients, and wastes for the whole volume of the cell.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The volume of
	cytoplasm determines the need for this exchange.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Rates of chemical
	exchange across the plasma membrane may be inadequate to maintain a
	cell with a very large cytoplasm.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The need for a
	surface sufficiently large to accommodate the volume explains the
	microscopic size of most cells.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Larger
	organisms do not generally have </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>larger</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	cells than smaller organisms—simply </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>more</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	cells.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cells that exchange
	a lot of material with their surroundings, such as intestinal cells,
	may have long, thin projections from the cell surface called
	microvilli. Microvilli increase surface area without significantly
	increasing cell volume.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Internal
membranes compartmentalize the functions of a eukaryotic cell.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A eukaryotic cell
	has extensive and elaborate internal membranes, which partition the
	cell into compartments.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These membranes
	also participate directly in metabolism, as many enzymes are built
	into membranes.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The compartments
	created by membranes provide different local environments that
	facilitate specific metabolic functions, allowing several
	incompatible processes to go on simultaneously in a cell.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The general
	structure of a biological membrane is a double layer of
	phospholipids.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Other lipids and
	diverse proteins are embedded in the lipid bilayer or attached to
	its surface.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each type of
	membrane has a unique combination of lipids and proteins for its
	specific functions.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For example,
	enzymes embedded in the membranes of mitochondria function in
	cellular respiration.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.17in; margin-bottom: 0in; line-height: 0.17in; text-decoration: none">
<BR>
</P>
<P STYLE="margin-top: 0.17in; margin-bottom: 0in; line-height: 0.17in; text-decoration: none">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Concept
6.3 The eukaryotic cell’s genetic instructions are housed in the
nucleus and carried out by the ribosomes</B></FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>nucleus</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	contains most of the genes in a eukaryotic cell.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Additional genes
	are located in mitochondria and chloroplasts.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The nucleus
	averages about 5 microns in diameter.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	nucleus is separated from the cytoplasm by a double membrane called
	the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>nuclear
	envelope.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The two membranes
	of the nuclear envelope are separated by 20–40 nm.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The envelope is
	perforated by pores that are about 100 nm in diameter.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>At the lip of each
	pore, the inner and outer membranes of the nuclear envelope are
	fused to form a continuous membrane.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A
	protein structure called a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>pore
	complex</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	lines each pore, regulating the passage of certain large
	macromolecules and particles.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	nuclear side of the envelope is lined by the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>nuclear
	lamina,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	a network of protein filaments that maintains the shape of the
	nucleus.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>There
	is evidence that a framework of fibers called the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>nuclear
	matrix</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	extends through the nuclear interior.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Within
	the nucleus, the DNA and associated proteins are organized into
	discrete units called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>chromosomes,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	structures that carry the genetic information.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each
	chromosome is made up of fibrous material called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>chromatin,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	a complex of proteins and DNA.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Stained chromatin
	appears through light microscopes and electron microscopes as a
	diffuse mass.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>As the cell
	prepares to divide, the chromatin fibers coil up and condense,
	becoming thick enough to be recognized as the familiar chromosomes.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each eukaryotic
	species has a characteristic number of chromosomes.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A typical human
	cell has 46 chromosomes.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A human sex cell
	(egg or sperm) has only 23 chromosomes.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	the nucleus is a region of densely stained fibers and granules
	adjoining chromatin, the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>nucleolus.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In the nucleolus,
	ribosomal RNA (rRNA) is synthesized and assembled with proteins from
	the cytoplasm to form ribosomal subunits.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The subunits pass
	through the nuclear pores to the cytoplasm, where they combine to
	form ribosomes.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The nucleus directs
	protein synthesis by synthesizing messenger RNA (mRNA).</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The mRNA travels to
	the cytoplasm through the nuclear pores and combines with ribosomes
	to translate its genetic message into the primary structure of a
	specific polypeptide.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Ribosomes
build a cell’s proteins.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Ribosomes,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	containing rRNA and protein, are the organelles that carry out
	protein synthesis.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cell types that
	synthesize large quantities of proteins (e.g., pancreas cells) have
	large numbers of ribosomes and prominent nucleoli.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Some
	ribosomes, </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>free</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>ribosomes,</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	are suspended in the cytosol and synthesize proteins that function
	within the cytosol.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Other
	ribosomes, </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>bound</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>ribosomes,</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	are attached to the outside of the endoplasmic reticulum or nuclear
	envelope.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These synthesize
	proteins that are either included in membranes or exported from the
	cell.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Ribosomes can shift
	between roles depending on the polypeptides they are synthesizing.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.17in; margin-bottom: 0in; line-height: 0.17in; text-decoration: none">
<BR>
</P>
<P STYLE="margin-top: 0.17in; margin-bottom: 0in; line-height: 0.17in; text-decoration: none">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Concept
6.4 The endomembrane system regulates protein traffic and performs
metabolic functions in the cell</B></FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Many
	of the internal membranes in a eukaryotic cell are part of the
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>endomembrane
	system.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These
	membranes are either directly continuous or connected via transfer
	of </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>vesicles,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	sacs of membrane.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In spite of these
	connections, these membranes are diverse in function and structure.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The thickness,
	molecular composition and types of chemical reactions carried out by
	proteins in a given membrane may be modified several times during a
	membrane’s life.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The endomembrane
	system includes the nuclear envelope, endoplasmic reticulum, Golgi
	apparatus, lysosomes, vacuoles, and the plasma membrane.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>The
endoplasmic reticulum manufactures membranes and performs many other
biosynthetic functions.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>endoplasmic
	reticulum (ER)</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	accounts for half the membranes in a eukaryotic cell.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The ER includes
	membranous tubules and internal, fluid-filled spaces called
	cisternae.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The ER membrane is
	continuous with the nuclear envelope, and the cisternal space of the
	ER is continuous with the space between the two membranes of the
	nuclear envelope.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>There are two
	connected regions of ER that differ in structure and function.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Smooth
	ER</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	looks smooth because it lacks ribosomes.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Rough
	ER</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	looks rough because ribosomes (bound ribosomes) are attached to the
	outside, including the outside of the nuclear envelope.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The smooth ER is
	rich in enzymes and plays a role in a variety of metabolic
	processes.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Enzymes of smooth
	ER synthesize lipids, including oils, phospholipids, and steroids.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These include the
	sex hormones of vertebrates and adrenal steroids.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In the smooth ER of
	the liver, enzymes help detoxify poisons and drugs such as alcohol
	and barbiturates.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Frequent use of
these drugs leads to the proliferation of smooth ER in liver cells,
increasing the rate of detoxification.</FONT></FONT></P>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This increases
tolerance to the target and other drugs, so higher doses are required
to achieve the same effect.</FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Smooth ER stores
	calcium ions.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Muscle cells have a
specialized smooth ER that pumps calcium ions from the cytosol and
stores them in its cisternal space.</FONT></FONT></P>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>When a nerve impulse
stimulates a muscle cell, calcium ions rush from the ER into the
cytosol, triggering contraction.</FONT></FONT></P>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Enzymes then pump
the calcium back, readying the cell for the next stimulation.</FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Rough ER is
	especially abundant in cells that secrete proteins.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>As a polypeptide is
	synthesized on a ribosome attached to rough ER, it is threaded into
	the cisternal space through a pore formed by a protein complex in
	the ER membrane.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>As it enters the
	cisternal space, the new protein folds into its native conformation.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most
	secretory polypeptides are </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>glycoproteins,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	proteins to which a carbohydrate is attached.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Secretory
	proteins are packaged in </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>transport
	vesicles</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	that carry them to their next stage.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Rough ER is also a
	membrane factory.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Membrane-bound
	proteins are synthesized directly into the membrane.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Enzymes in the
	rough ER also synthesize phospholipids from precursors in the
	cytosol.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>As the ER membrane
	expands, membrane can be transferred as transport vesicles to other
	components of the endomembrane system.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>The Golgi
apparatus is the shipping and receiving center for cell products.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Many
	transport vesicles from the ER travel to the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Golgi
	apparatus</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	for modification of their contents.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The Golgi is a
	center of manufacturing, warehousing, sorting, and shipping.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The Golgi apparatus
	is especially extensive in cells specialized for secretion.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The Golgi apparatus
	consists of flattened membranous sacs—cisternae—looking like a
	stack of pita bread.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The membrane of
	each cisterna separates its internal space from the cytosol.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>One
	side of the Golgi, the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>cis</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	side, is located near the ER. The </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>cis</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	face receives material by fusing with transport vesicles from the
	ER.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	other side, the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>trans</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	side, buds off vesicles that travel to other sites.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>During
	their transit from the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>cis</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	to the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>trans</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	side, products from the ER are usually modified.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The Golgi can also
	manufacture its own macromolecules, including pectin and other
	noncellulose polysaccharides.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The Golgi apparatus
	is a very dynamic structure.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>According
	to the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>cisternal
	maturation model,</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	the cisternae of the Golgi progress from the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>cis</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	to the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>trans</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	face, carrying and modifying their protein cargo as they move.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Finally, the Golgi
	sorts and packages materials into transport vesicles.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Molecular
	identification tags are added to products to aid in sorting.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Products are tagged
	with identifiers such as phosphate groups. These act like ZIP codes
	on mailing labels to identify the product’s final destination.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Lysosomes are
digestive compartments.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>lysosome</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is a membrane-bound sac of hydrolytic enzymes that an animal cell
	uses to digest macromolecules.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Lysosomal enzymes
	can hydrolyze proteins, fats, polysaccharides, and nucleic acids.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These enzymes work
	best at pH 5.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Proteins in the
	lysosomal membrane pump hydrogen ions from the cytosol into the
	lumen of the lysosomes.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Rupture of one or a
	few lysosomes has little impact on a cell because the lysosomal
	enzymes are not very active at the neutral pH of the cytosol.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>However, massive
	rupture of many lysosomes can destroy a cell by autodigestion.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Lysosomal enzymes
	and membrane are synthesized by rough ER and then transferred to the
	Golgi apparatus for further modification.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Proteins on the
	inner surface of the lysosomal membrane are spared by digestion by
	their three-dimensional conformations, which protect vulnerable
	bonds from hydrolysis.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Lysosomes carry out
	intracellular digestion in a variety of circumstances.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>Amoebas</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	eat by engulfing smaller organisms by </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>phagocytosis.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The food vacuole
	formed by phagocytosis fuses with a lysosome, whose enzymes digest
	the food.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>As the polymers are
	digested, monomers pass to the cytosol to become nutrients for the
	cell.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Lysosomes can play
	a role in recycling of the cell’s organelles and macromolecules.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This
	recycling, or </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>autophagy,</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	renews the cell.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>During autophagy, a
	damaged organelle or region of cytosol becomes surrounded by
	membrane.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A lysosome fuses
	with the resulting vesicle, digesting the macromolecules and
	returning the organic monomers to the cytosol for reuse.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The lysosomes play
	a critical role in the programmed destruction of cells in
	multicellular organisms.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This process plays
	an important role in development.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The hands of human
	embryos are webbed until lysosomes digest the cells in the tissue
	between the fingers.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This
	important process is called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>programmed
	cell death</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>,
	or </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>apoptosis.</B></FONT></FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Vacuoles have
diverse functions in cell maintenance.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Vesicles and
	vacuoles (larger versions) are membrane-bound sacs with varied
	functions.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Food
	vacuoles</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	are formed by phagocytosis and fuse with lysosomes.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Contractile
	vacuoles,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	found in freshwater protists, pump excess water out of the cell to
	maintain the appropriate concentration of salts.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A
	large </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>central
	vacuole</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is found in many mature plant cells.</FONT></FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
membrane surrounding the central vacuole, the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>tonoplast,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
is selective in its transport of solutes into the central vacuole.</FONT></FONT></FONT></P>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The functions of the
central vacuole include stockpiling proteins or inorganic ions,
disposing of metabolic byproducts, holding pigments, and storing
defensive compounds that defend the plant against herbivores.</FONT></FONT></P>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Because of the large
vacuole, the cytosol occupies only a thin layer between the plasma
membrane and the tonoplast. The presence of a large vacuole increases
surface area to volume ratio for the cell.</FONT></FONT></P>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
<BR>
</P>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; font-style: normal; line-height: 0.17in">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Concept
6.5 Mitochondria and chloroplasts change energy from one form to
another</B></FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Mitochondria and
	chloroplasts are the organelles that convert energy to forms that
	cells can use for work.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Mitochondria</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	are the sites of cellular respiration, generating ATP from the
	catabolism of sugars, fats, and other fuels in the presence of
	oxygen.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Chloroplasts,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	found in plants and algae, are the sites of photosynthesis.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>They
	convert solar energy to chemical energy and synthesize new organic
	compounds such as sugars from CO</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	and H</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>O.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Mitochondria and
	chloroplasts are not part of the endomembrane system.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In contrast to
	organelles of the endomembrane system, each mitochondrion or
	chloroplast has two membranes separating the innermost space from
	the cytosol.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Their membrane
	proteins are not made by the ER, but rather by free ribosomes in the
	cytosol and by ribosomes within the organelles themselves.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Both organelles
	have small quantities of DNA that direct the synthesis of the
	polypeptides produced by these internal ribosomes.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Mitochondria and
	chloroplasts grow and reproduce as semiautonomous organelles.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Almost all
	eukaryotic cells have mitochondria.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>There may be one
	very large mitochondrion or hundreds to thousands of individual
	mitochondria.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The number of
	mitochondria is correlated with aerobic metabolic activity.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A typical
	mitochondrion is 1–10 microns long.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Mitochondria are
	quite dynamic: moving, changing shape, and dividing.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Mitochondria
	have a smooth outer membrane and a convoluted inner membrane with
	infoldings called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>cristae.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The inner membrane
	divides the mitochondrion into two internal compartments.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The first is the
	intermembrane space, a narrow region between the inner and outer
	membranes.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	inner membrane encloses the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>mitochondrial
	matrix,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	a fluid-filled space with DNA, ribosomes, and enzymes.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Some of the
	metabolic steps of cellular respiration are catalyzed by enzymes in
	the matrix.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The cristae present
	a large surface area for the enzymes that synthesize ATP.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	chloroplast is one of several members of a generalized class of
	plant structures called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>plastids.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Amyloplasts are
	colorless plastids that store starch in roots and tubers.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Chromoplasts store
	pigments for fruits and flowers.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Chloroplasts
	contain the green pigment chlorophyll as well as enzymes and other
	molecules that function in the photosynthetic production of sugar.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
