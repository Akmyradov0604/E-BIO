<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT SIZE=4 STYLE="font-size: 16pt"><B><FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><SPAN STYLE="text-decoration: none">Chapter
2   The Chemical Context of Life</SPAN></FONT></FONT></B></FONT></P>
<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Lecture
Outline</B></I></FONT></FONT></P>
<P STYLE="margin-top: 0.22in; margin-bottom: 0.17in; text-decoration: none">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Overview:
Chemical Foundations of Biology</B></FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Living organisms
	and the world they live in are subject to the basic laws of physics
	and chemistry.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Biology is a
	multidisciplinary science, drawing on insights from other sciences.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Life can be
	organized into a hierarchy of structural levels.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>At each successive
	level, additional emergent properties appear.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; font-style: normal; line-height: 0.17in">
<BR>
</P>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
<FONT SIZE=2 STYLE="font-size: 11pt"><B><FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><SPAN STYLE="font-style: normal">Concept
2.1 Matter consists of chemical elements in pure form and in
combinations called compounds</SPAN></FONT></FONT></FONT></B></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Organisms
	are composed of </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>matter.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Matter is anything
	that takes up space and has mass.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Matter is made up
	of elements.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>An
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>element</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is a substance that cannot be broken down into other substances by
	chemical reactions.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>There are 92
	naturally occurring elements.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each element has a
	unique symbol, usually the first one or two letters of the name.
	Some of the symbols are derived from Latin or German names.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>compound</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is a substance consisting of two or more elements in a fixed ratio.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Table salt (sodium
	chloride or NaCl) is a compound with equal numbers of atoms of the
	elements chlorine and sodium.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>While pure sodium
	is a metal and chlorine is a gas, they combine to form an edible
	compound. This change in characteristics when elements combine to
	form a compound is an example of an emergent property.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
 <FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>25 chemical
elements are essential to life.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>About 25 of the 92
	natural elements are known to be essential for life.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Four
	elements—carbon (C), oxygen (O), hydrogen (H), and nitrogen
	(N)—make up 96% of living matter.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most of the
	remaining 4% of an organism’s weight consists of phosphorus (P),
	sulfur (S), calcium (Ca), and potassium (K).</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Trace
	elements</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	are required by an organism but only in minute quantities.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Some trace
	elements, like iron (Fe), are required by all organisms.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Other trace
	elements are required by only some species.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For example, a daily
intake of 0.15 milligrams of iodine is required for normal activity
of the human thyroid gland.</FONT></FONT></P>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
<BR>
</P>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; font-style: normal; line-height: 0.17in">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Concept
2.2 An element’s properties depend on the structure of its atoms</B></FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each element
	consists of unique atoms.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>An
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>atom</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is the smallest unit of matter that still retains the properties of
	an element.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Atoms are composed
	of even smaller parts, called subatomic particles.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Two of these,
	neutrons and protons, are packed together to form a dense core, the
	atomic nucleus, at the center of an atom.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Electrons can be
	visualized as forming a cloud of negative charge around the nucleus.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each electron has
	one unit of negative charge.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each proton has one
	unit of positive charge.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Neutrons
	are electrically neutral.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The attractions
	between the positive charges in the nucleus and the negative charges
	of the electrons keep the electrons in the vicinity of the nucleus.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A
	neutron and a proton are almost identical in mass, about 1.7 ×
	10</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>−24</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	gram per particle.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For
	convenience, a smaller unit of measure, the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>dalton,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is used to measure the mass of subatomic particles, atoms, or
	molecules.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The mass of a
	neutron or a proton is close to 1 dalton.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The mass of an
	electron is about 1/2000 that of a neutron or proton.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Therefore, we
	typically ignore the contribution of electrons when determining the
	total mass of an atom.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>All atoms of a
	particular element have the same number of protons in their nuclei.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This number of
	protons is the element’s unique atomic number.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	atomic number is written as a subscript before the symbol for the
	element. For example, </FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>He
	means that an atom of helium has 2 protons in its nucleus.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Unless otherwise
	indicated, atoms have equal numbers of protons and electrons and,
	therefore, no net charge.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Therefore, the
	atomic number tells us the number of protons and the number of
	electrons that are found in a neutral atom of a specific element.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>mass
	number</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is the sum of the number of protons and neutrons in the nucleus of
	an atom.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Therefore, we can
	determine the number of neutrons in an atom by subtracting the
	number of protons (the atomic number) from the mass number.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	mass number is written as a superscript before an element’s symbol
	(for example, </FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>4</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>He).</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>atomic
	weight</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	of an atom, a measure of its mass, can be approximated by the mass
	number.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For
	example, </FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>4</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>He
	has a mass number of 4 and an estimated atomic weight of 4 daltons.
	More precisely, its atomic weight is 4.003 daltons.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>While all atoms of
	a given element have the same number of protons, they may differ in
	the number of neutrons.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Two
	atoms of the same element that differ in the number of neutrons are
	called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>isotopes.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In nature, an
	element occurs as a mixture of isotopes.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For
	example, 99% of carbon atoms have 6 neutrons (</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>12</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>C).</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most
	of the remaining 1% of carbon atoms have 7 neutrons (</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>13</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>C)
	while the rarest carbon isotope, with 8 neutrons, is </FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>14</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>C.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most isotopes are
	stable; they do not tend to lose particles.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Both
	</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>12</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>C
	and </FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>13</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>C
	are stable isotopes.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The nuclei of some
	isotopes are unstable and decay spontaneously, emitting particles
	and energy.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>14</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>C
	is one of these unstable isotopes, or radioactive isotopes.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>When
	</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>14</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>C
	decays, one of its neutrons is converted to a proton and an
	electron.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This
	converts </FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>14</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>C
	to </FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>14</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>N,
	transforming the atom to a different element.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Radioactive
	isotopes have many applications in biological research.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Radioactive decay
	rates can be used to date fossils.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Radioactive
	isotopes can be used to trace atoms through metabolic processes.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Radioactive
	isotopes are also used to diagnose medical disorders.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For example, a
	known quantity of a substance labeled with a radioactive isotope can
	be injected into the blood, and its rate of excretion in the urine
	can be measured.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Also, radioactive
	tracers can be used with imaging instruments to monitor chemical
	processes in the body.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>While useful in
	research and medicine, the energy emitted in radioactive decay is
	hazardous to life.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This energy can
	destroy molecules within living cells.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The severity of
	damage depends on the type and amount of radiation that the organism
	absorbs.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Electron
configuration influences the chemical behavior of an atom.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Simplified models
	of the atom greatly distort the atom’s relative dimensions.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>To gain an accurate
	perspective of the relative proportions of an atom, if the nucleus
	was the size of a golf ball, the electrons would be moving about 1
	kilometer from the nucleus.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Atoms are mostly
	empty space.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>When two elements
	interact during a chemical reaction, it is actually their electrons
	that are involved.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The nuclei do not
	come close enough to interact.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The electrons of an
	atom vary in the amount of energy they possess.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Energy</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is the ability to do work.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Potential
	energy</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is the energy that matter stores because of its position or
	location.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water stored behind
	a dam has potential energy that can be used to do work turning
	electric generators.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Because potential
	energy has been expended, the water stores less energy at the bottom
	of the dam than it did in the reservoir.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Electrons have
	potential energy because of their position relative to the nucleus.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The negatively
	charged electrons are attracted to the positively charged nucleus.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The farther
	electrons are from the nucleus, the more potential energy they have.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Changes in an
	electron’s potential energy can only occur in steps of a fixed
	amount, moving the electron to a fixed location relative to the
	nucleus.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>An electron cannot
	exist between these fixed locations.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	different states of potential energy that the electrons of an atom
	can have are called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>energy
	levels</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	or </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>electron
	shells.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The first shell,
	closest to the nucleus, has the lowest potential energy.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Electrons in outer
	shells have more potential energy.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Electrons can
	change their position only if they absorb or release a quantity of
	energy that matches the difference in potential energy between the
	two levels.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The chemical
	behavior of an atom is determined by its electron configuration—the
	distribution of electrons in its electron shells.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The first 18
	elements, including those most important in biological processes,
	can be arranged in 8 columns and 3 rows.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Elements in the same
row fill the same shells with electrons.</FONT></FONT></P>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Moving from left to
right, each element adds one electron (and proton) from the element
before.</FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The first electron
	shell can hold only 2 electrons.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The two electrons
	of helium fill the first shell.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Atoms with more
	than two electrons must place the extra electrons in higher shells.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For example,
	lithium, with three electrons, has two in the first shell and one in
	the second shell.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The second shell
	can hold up to 8 electrons.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Neon, with 10 total
	electrons, has two in the first shell and eight in the second,
	filling both shells.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	chemical behavior of an atom depends mostly on the number of
	electrons in its outermost shell, the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>valence
	shell.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Electrons
	in the valence shell are known as </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>valence
	electrons.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Lithium has one
	valence electron; neon has eight.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Atoms with the same
	number of valence electrons have similar chemical behaviors.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>An atom with a
	completed valence shell, like neon, is nonreactive.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>All other atoms are
	chemically reactive because they have incomplete valence shells.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The paths of
	electrons are often portrayed as concentric paths, like planets
	orbiting the sun.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	reality, an electron occupies a more complex three-dimensional
	space, an </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>orbital.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The orbital
	represents the space in which the electron is found 90% of the time.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Each orbital can
	hold a maximum of two electrons.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	first shell has room for a single spherical 1</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>s
	</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>orbital
	for its pair of electrons.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	second shell can pack pairs of electrons into a spherical 2</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>s
	</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>orbital
	and three dumbbell-shaped 2</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>p</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	orbitals.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The reactivity of
	atoms arises from the presence of unpaired electrons in one or more
	orbitals of their valence shells.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Electrons occupy
	separate orbitals within the valence shell until forced to share
	orbitals.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The four valence
electrons of carbon each occupy separate orbitals, but the five
valence electrons of nitrogen are distributed into three unshared
orbitals and one shared orbital.</FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>When
	atoms interact to complete their valence shells, it is the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>unpaired</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	electrons that are involved.</FONT></FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
<BR>
</P>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; font-style: normal; line-height: 0.17in">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Concept
2.3 The formation and function of molecules depend on chemical
bonding between atoms</B></FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Atoms with
	incomplete valence shells can interact with each other by sharing or
	transferring valence electrons.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These
	interactions typically result in the atoms remaining close together,
	held by attractions called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>chemical
	bonds.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The strongest
	chemical bonds are covalent bonds and ionic bonds.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>covalent
	bond</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is formed by the sharing of a pair of valence electrons by two
	atoms.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>If two atoms come
	close enough that their unshared orbitals overlap, they will share
	their newly paired electrons. Each atom can count both electrons
	toward its goal of filling the valence shell.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For
	example, if two hydrogen atoms come close enough that their 1</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>s</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	orbitals overlap, then they can share a pair of electrons, with each
	atom contributing one.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Two
	or more atoms held together by covalent bonds constitute a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>molecule.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>We
	can abbreviate the structure of the molecule by substituting a line
	for each pair of shared electrons, drawing the </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>structural
	formula.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>H—H is the
	structural formula for the covalent bond between two hydrogen atoms.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>molecular
	formula</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	indicates the number and types of atoms present in a single
	molecule.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>H</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	is the molecular formula for hydrogen gas.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Oxygen needs to add
	2 electrons to the 6 already present to complete its valence shell.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Two
	oxygen atoms can form a molecule by sharing </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>two</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	pairs of valence electrons.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These
	atoms have formed a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>double
	covalent bond.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Every
	atom has a characteristic total number of covalent bonds that it can
	form, equal to the number of unpaired electrons in the outermost
	shell. This bonding capacity is called the atom’s </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>valence.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The valence of
	hydrogen is 1.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Oxygen is 2.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Nitrogen is 3.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Carbon is 4.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Phosphorus should
	have a valence of 3, based on its three unpaired electrons, but in
	biological molecules it generally has a valence of 5, forming three
	single covalent bonds and one double bond.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Covalent bonds can
	form between atoms of the same element or atoms of different
	elements.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>While both types
	are molecules, the latter are also compounds.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water,
	H</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>O,
	is a compound in which two hydrogen atoms form single covalent bonds
	with an oxygen atom.</FONT></FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This satisfies the
valences of both elements.</FONT></FONT></P>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Methane,
CH</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>4</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>,
satisfies the valences of both C and H.</FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	attraction of an atom for the shared electrons of a covalent bond is
	called its </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>electronegativity.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Strongly
	electronegative atoms attempt to pull the shared electrons toward
	themselves.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>If
	electrons in a covalent bond are shared equally, then this is a
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>nonpolar
	covalent bond.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A covalent bond
	between two atoms of the same element is always nonpolar.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A covalent bond
	between atoms that have similar electronegativities is also
	nonpolar.</FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Because
carbon and hydrogen do not differ greatly in electronegativities, the
bonds of CH</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>4</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
are nonpolar.</FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>When
	two atoms that differ in electronegativity bond, they do not share
	the electron pair equally and form a </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>polar
	covalent bond.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The bonds between
	oxygen and hydrogen in water are polar covalent because oxygen has a
	much higher electronegativity than does hydrogen.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Compounds with a
	polar covalent bond have regions of partial negative charge near the
	strongly electronegative atom and regions of partial positive charge
	near the weakly electronegative atom.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>An
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>ionic
	bond</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	can form if two atoms are so unequal in their attraction for valence
	electrons that one atom strips an electron completely from the
	other.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For example,
	sodium, with one valence electron in its third shell, transfers this
	electron to chlorine, with 7 valence electrons in its third shell.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Now, sodium has a
	full valence shell (the second) and chlorine has a full valence
	shell (the third).</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>After
	the transfer, both atoms are no longer neutral, but have charges and
	are called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>ions.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Sodium has one more
	proton than electrons and has a net positive charge.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Atoms
	with positive charges are </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>cations.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Chlorine has one
	more electron than protons and has a net negative charge.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Atoms
	with negative charges are </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>anions.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Because
	of differences in charge, cations and anions are attracted to each
	other to form an </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>ionic
	bond.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Atoms in an ionic
	bond need not have acquired their charges by transferring electrons
	with each other.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Compounds
	formed by ionic bonds are </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>ionic
	compounds,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	or </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>salts.</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	An example is NaCl, or table salt.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	formula for an ionic compound indicates the ratio of elements in a
	crystal of that salt. NaCl is not a molecule, but a salt crystal
	with equal numbers of Na</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>+</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	and Cl</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>−</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	ions.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Ionic compounds can
	have ratios of elements different from 1:1.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For
	example, the ionic compound magnesium chloride (MgCl</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>)
	has 2 chloride atoms per magnesium atom.</FONT></FONT></FONT></P>
</UL>
<P STYLE="margin-left: 0.75in; text-indent: -0.25in; margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Magnesium
needs to lose 2 electrons to drop to a full outer shell; each
chlorine atom needs to gain 1.</FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Entire molecules
	that have full electrical charges are also called ions.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	the salt ammonium chloride (NH</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>4</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Cl),
	the anion is Cl</FONT></FONT><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>−</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	and the cation is NH</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>4</FONT></FONT></SUB><SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>+</FONT></FONT></SUP><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The strength of
	ionic bonds depends on environmental conditions, such as moisture.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Water
	can dissolve salts by reducing the attraction between the salt’s
	anions and cations.</FONT></FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>Weak chemical
bonds play important roles in the chemistry of life.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Within a cell,
	weak, brief bonds between molecules are important to a variety of
	processes.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For example, signal
	molecules from one neuron use weak bonds to bind briefly to receptor
	molecules on the surface of a receiving neuron.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This triggers a
	response by the recipient.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Weak interactions
	include ionic bonds (weak in water), hydrogen bonds, and van der
	Waals interactions.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Hydrogen
	bonds</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	form when a hydrogen atom already covalently bonded to a strongly
	electronegative atom is attracted to another strongly
	electronegative atom.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These strongly
	electronegative atoms are typically nitrogen or oxygen.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>These bonds form
	because a polar covalent bond leaves the hydrogen atom with a
	partial positive charge and the other atom with a partial negative
	charge.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The partially
	positive–charged hydrogen atom is attracted to regions of full or
	partial negative charge on molecules, atoms, or even regions of the
	same large molecule.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For example,
	ammonia molecules and water molecules interact with weak hydrogen
	bonds.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In the ammonia
	molecule, the hydrogen atoms have partial positive charges, and the
	more electronegative nitrogen atom has a partial negative charge.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In the water
	molecule, the hydrogen atoms also have partial positive charges, and
	the oxygen atom has a partial negative charge.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Areas with opposite
	charges are attracted.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Even molecules with
	nonpolar covalent bonds can have temporary regions of partial
	negative and positive charge.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Because electrons
	are constantly in motion, there can be periods when they accumulate
	by chance in one area of a molecule.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This creates
	ever-changing regions of partial negative and positive charge within
	a molecule.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Molecules
	or atoms in close proximity can be attracted by these fleeting
	charge differences, creating </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>van
	der Waals interactions.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>While individual
	bonds (ionic, hydrogen, van der Waals) are weak and temporary,
	collectively they are strong and play important biological roles.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; line-height: 0.17in">
<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I><B>A molecule’s
biological function is related to its shape.</B></I></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	three-dimensional shape of a molecule is an important determinant of
	its function in a cell.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>A molecule with two
	atoms is always linear.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>However, a molecule
	with more than two atoms has a more complex shape.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The shape of a
	molecule is determined by the positions of the electron orbitals
	that are shared by the atoms involved in the bond.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>When covalent bonds
	form, the orbitals in the valence shell of each atom rearrange.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For
	atoms with electrons in both </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>s</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	and </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><I>p</I></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	orbitals, the formation of a covalent bonds leads to hybridization
	of the orbitals to four new orbitals in a tetrahedral shape.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In a water
	molecule, two of oxygen’s four hybrid orbitals are shared with
	hydrogen atoms. The water molecule is shaped like a V, with its two
	covalent bonds spread apart at an angle of 104.5°.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	a methane molecule (CH</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>4</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>),
	the carbon atom shares all four of its hybrid orbitals with H atoms.
	The carbon nucleus is at the center of the tetrahedron, with
	hydrogen nuclei at the four corners.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Large organic
	molecules contain many carbon atoms. In these molecules, the
	tetrahedral shape of carbon bonded to four other atoms is often a
	repeating motif.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Biological
	molecules recognize and interact with one another with a specificity
	based on molecular shape.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For example, signal
	molecules from a transmitting cell have specific shapes that bind to
	complementary receptor molecules on the surface of the receiving
	cell.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The temporary
	attachment of the receptor and signal molecule stimulates activity
	in the receptor cell.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Molecules with
	similar shapes can have similar biological effects.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For example,
	morphine, heroin, and other opiate drugs are similar enough in shape
	that they can bind to the same receptors as natural signal molecules
	called endorphins.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Binding of
	endorphins to receptors on brain cells produces euphoria and
	relieves pain. Opiates mimic these natural endorphin effects.</FONT></FONT></P>
</UL>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; font-style: normal; line-height: 0.17in">
<BR>
</P>
<P STYLE="margin-top: 0.13in; margin-bottom: 0in; font-style: normal; line-height: 0.17in">
<FONT COLOR="#ff0000"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>Concept
2.4 Chemical reactions make and break chemical bonds</B></FONT></FONT></FONT></P>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>chemical
	reactions,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	chemical bonds are broken and reformed, leading to new arrangements
	of atoms.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	starting molecules in the process are called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>reactants,</B></FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	and the final molecules are called </FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>products.</B></FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In a chemical
	reaction, all of the atoms in the reactants must be present in the
	products.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The reactions must
	be “balanced”.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Matter is conserved
	in a chemical reaction.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Chemical reactions
	rearrange matter; they do not create or destroy matter.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For
	example, we can recombine the covalent bonds of H</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	and O</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	to form the new bonds of H</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>O.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>In
	this reaction, two molecules of H</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	combine with one molecule of O</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	to form two molecules of H</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>O.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Photosynthesis is
	an important chemical reaction.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Humans and other
	animals ultimately depend on photosynthesis for food and oxygen.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Green
	plants combine carbon dioxide (CO</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>)
	from the air and water (H</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>O)
	from the soil to create sugar molecules and release molecular oxygen
	(O</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>)
	as a by-product.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This chemical
	reaction is powered by sunlight.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>The
	overall process of photosynthesis is 6CO</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	+ 6H</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>O
	--&gt; C</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>6</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>H</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>12</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>O</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>6</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	+ 6O</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>.</FONT></FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>This process occurs
	in a sequence of individual chemical reactions that rearrange the
	atoms of the reactants to form the products.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Some chemical
	reactions go to completion; that is, all the reactants are converted
	to products.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Most chemical
	reactions are reversible, with the products in the forward reaction
	becoming the reactants for the reverse reaction.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>For
	example in this reaction: 3H</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	+ N</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>2</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	&lt;=&gt; 2NH</FONT></FONT><SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>3</FONT></FONT></SUB><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>
	hydrogen and nitrogen molecules combine to form ammonia, but ammonia
	can decompose to hydrogen and nitrogen molecules.</FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Initially, when
	reactant concentrations are high, they frequently collide to create
	products.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>As products
	accumulate, they collide to reform reactants.</FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.08in; margin-bottom: 0in; line-height: 0.17in">
	<FONT SIZE=2 STYLE="font-size: 11pt"><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>Eventually,
	the rate of formation of products is the same as the rate of
	breakdown of products (formation of reactants), and the system is at
	</FONT></FONT><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4><B>chemical
	equilibrium.</B></FONT></FONT></FONT></P>
</UL>
<UL>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>At equilibrium,
	products and reactants are continually being formed, but there is no
	net change in the concentrations of reactants and products.</FONT></FONT></P>
	<LI><P STYLE="margin-top: 0.04in; margin-bottom: 0in; line-height: 0.17in">
	<FONT FACE="Comic Sans MS, cursive"><FONT SIZE=4>At equilibrium, the
	concentrations of reactants and products are typically not equal,
	but their concentrations have stabilized at a particular ratio.</FONT></FONT></P>
</UL>
